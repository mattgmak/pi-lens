#!/usr/bin/env node
// Generate per-language catalogs of the bundled lint rules:
//   docs/ast-grep_rules_catalog.md     — ast-grep YAML rules (pi-lens + CodeRabbit)
//   docs/tree-sitter_rules_catalog.md  — tree-sitter query rules
//
//   node scripts/gen-rule-catalogs.mjs           # write the catalogs
//   node scripts/gen-rule-catalogs.mjs --check    # fail (exit 1) if out of date
//
// The catalogs are GENERATED — edit the rule files, not the docs. A `--check`
// run in CI / a sync test keeps them from drifting.

import {
  readFileSync,
  writeFileSync,
  readdirSync,
  existsSync,
  statSync,
} from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, relative } from "node:path";
import yaml from "js-yaml";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const AST_DIR = join(ROOT, "rules", "ast-grep-rules");
const TS_DIR = join(ROOT, "rules", "tree-sitter-queries");
const AST_OUT = join(ROOT, "docs", "ast-grep_rules_catalog.md");
const TS_OUT = join(ROOT, "docs", "tree-sitter_rules_catalog.md");

const GENERATED_NOTE =
  "<!-- GENERATED by scripts/gen-rule-catalogs.mjs — do not edit by hand. " +
  "Run `npm run docs:rule-catalogs` after changing rules. -->";

// ---- helpers ---------------------------------------------------------------

function walkYaml(dir) {
  if (!existsSync(dir)) return [];
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    const st = statSync(p);
    if (st.isDirectory()) out.push(...walkYaml(p));
    else if (name.endsWith(".yml") || name.endsWith(".yaml")) out.push(p);
  }
  return out;
}

function loadRule(path) {
  try {
    const doc = yaml.load(readFileSync(path, "utf8"));
    return doc && typeof doc === "object" ? doc : null;
  } catch {
    return null;
  }
}

// Collapse a YAML block scalar to a single, table-safe markdown cell. Escape
// the backslash FIRST, then the pipe, so a literal `\` in the input (e.g. a
// Windows path like `src\win\async.c`) can't combine with the pipe-escape.
function cell(text, max = 140) {
  let s = String(text ?? "")
    .replace(/\s+/g, " ")
    .trim();
  if (s.length > max) s = s.slice(0, max - 1).trimEnd() + "…";
  return s.replace(/\\/g, "\\\\").replace(/\|/g, "\\|");
}

function titleCase(lang) {
  const map = {
    cpp: "C++",
    csharp: "C#",
    css: "CSS",
    html: "HTML",
    php: "PHP",
    tsx: "TSX",
    plsql: "PL/SQL",
    abap: "ABAP",
    cobol: "COBOL",
    javascript: "JavaScript",
    typescript: "TypeScript",
  };
  if (map[lang]) return map[lang];
  return lang.charAt(0).toUpperCase() + lang.slice(1);
}

function byLanguage(rules) {
  const groups = new Map();
  for (const r of rules) {
    const lang = (r.language || "unknown").toLowerCase();
    if (!groups.has(lang)) groups.set(lang, []);
    groups.get(lang).push(r);
  }
  for (const list of groups.values())
    list.sort((a, b) => String(a.id).localeCompare(String(b.id)));
  return [...groups.entries()].sort((a, b) => a[0].localeCompare(b[0]));
}

// ---- ast-grep catalog ------------------------------------------------------

function collectAstGrep() {
  const sets = [
    { dir: join(AST_DIR, "rules"), source: "pi-lens", status: "enabled" },
    {
      dir: join(AST_DIR, "rules-disabled"),
      source: "pi-lens",
      status: "disabled",
    },
    {
      dir: join(AST_DIR, "coderabbit", "rules"),
      source: "CodeRabbit",
      status: "enabled",
    },
  ];
  const rules = [];
  for (const set of sets) {
    for (const path of walkYaml(set.dir)) {
      const doc = loadRule(path);
      if (!doc || !doc.id || !doc.language) continue;
      rules.push({
        id: doc.id,
        language: doc.language,
        severity: doc.severity || "—",
        message: doc.message || doc.note || "",
        source: set.source,
        status: set.status,
      });
    }
  }
  return rules;
}

function renderAstGrepTable(rules) {
  const lines = ["| Rule | Severity | Source | Description |", "|---|---|---|---|"];
  for (const r of rules) {
    lines.push(
      `| \`${r.id}\` | ${r.severity} | ${r.source} | ${cell(r.message)} |`,
    );
  }
  return lines.join("\n");
}

function renderAstGrepCatalog(rules) {
  const enabled = rules.filter((r) => r.status === "enabled");
  const disabled = rules.filter((r) => r.status === "disabled");
  const piCount = enabled.filter((r) => r.source === "pi-lens").length;
  const crCount = enabled.filter((r) => r.source === "CodeRabbit").length;

  const out = [
    "# ast-grep rules catalog",
    "",
    GENERATED_NOTE,
    "",
    `pi-lens bundles **${enabled.length} enabled** ast-grep rules ` +
      `(${piCount} pi-lens-authored + ${crCount} vendored from CodeRabbit) ` +
      `across **${byLanguage(enabled).length} languages**, plus ${disabled.length} disabled.`,
    "",
    "See [`docs/custom-rules.md`](custom-rules.md) to add your own. " +
      "Rule sources: `rules/ast-grep-rules/rules/` (pi-lens), " +
      "`rules/ast-grep-rules/coderabbit/rules/` (vendored).",
    "",
    "## Enabled rules",
    "",
  ];
  for (const [lang, list] of byLanguage(enabled)) {
    out.push(`### ${titleCase(lang)} (${list.length})`, "");
    out.push(renderAstGrepTable(list), "");
  }
  if (disabled.length) {
    out.push("## Disabled rules", "");
    out.push(
      "Shipped but not dispatched (in `rules-disabled/`); enable by moving into `rules/`.",
      "",
    );
    for (const [lang, list] of byLanguage(disabled)) {
      out.push(`### ${titleCase(lang)} (${list.length})`, "");
      out.push(renderAstGrepTable(list), "");
    }
  }
  return out.join("\n").trimEnd() + "\n";
}

// ---- tree-sitter catalog ---------------------------------------------------

function collectTreeSitter() {
  const rules = [];
  if (!existsSync(TS_DIR)) return rules;
  for (const name of readdirSync(TS_DIR)) {
    const dir = join(TS_DIR, name);
    if (!statSync(dir).isDirectory()) continue;
    const disabled = name.endsWith("-disabled");
    const language = disabled ? name.replace(/-disabled$/, "") : name;
    for (const path of walkYaml(dir)) {
      const doc = loadRule(path);
      if (!doc || !doc.id) continue;
      rules.push({
        id: doc.id,
        name: doc.name || doc.id,
        language,
        severity: doc.severity || "—",
        category: doc.category || doc.defect_class || "—",
        message: doc.message || doc.description || "",
        status: disabled ? "disabled" : "enabled",
      });
    }
  }
  return rules;
}

function renderTreeSitterTable(rules) {
  const lines = [
    "| Rule | Severity | Category | Description |",
    "|---|---|---|---|",
  ];
  for (const r of rules) {
    lines.push(
      `| \`${r.id}\` | ${r.severity} | ${cell(r.category, 24)} | ${cell(r.message)} |`,
    );
  }
  return lines.join("\n");
}

function renderTreeSitterCatalog(rules) {
  const enabled = rules.filter((r) => r.status === "enabled");
  const disabled = rules.filter((r) => r.status === "disabled");
  const out = [
    "# tree-sitter rules catalog",
    "",
    GENERATED_NOTE,
    "",
    `pi-lens bundles **${enabled.length} enabled** tree-sitter query rules ` +
      `across **${byLanguage(enabled).length} languages**, plus ${disabled.length} disabled.`,
    "",
    "Rule source: `rules/tree-sitter-queries/<language>/`.",
    "",
    "## Enabled rules",
    "",
  ];
  for (const [lang, list] of byLanguage(enabled)) {
    out.push(`### ${titleCase(lang)} (${list.length})`, "");
    out.push(renderTreeSitterTable(list), "");
  }
  if (disabled.length) {
    out.push("## Disabled rules", "");
    for (const [lang, list] of byLanguage(disabled)) {
      out.push(`### ${titleCase(lang)} (${list.length})`, "");
      out.push(renderTreeSitterTable(list), "");
    }
  }
  return out.join("\n").trimEnd() + "\n";
}

// ---- main ------------------------------------------------------------------

function main() {
  const check = process.argv.includes("--check");
  const targets = [
    { out: AST_OUT, content: renderAstGrepCatalog(collectAstGrep()) },
    { out: TS_OUT, content: renderTreeSitterCatalog(collectTreeSitter()) },
  ];

  let stale = false;
  for (const t of targets) {
    const rel = relative(ROOT, t.out).replace(/\\/g, "/");
    if (check) {
      const current = existsSync(t.out) ? readFileSync(t.out, "utf8") : "";
      if (current !== t.content) {
        stale = true;
        console.error(`OUT OF DATE: ${rel} — run \`npm run docs:rule-catalogs\``);
      } else {
        console.log(`ok: ${rel}`);
      }
    } else {
      writeFileSync(t.out, t.content, "utf8");
      console.log(`wrote ${rel}`);
    }
  }
  if (check && stale) process.exit(1);
}

main();
